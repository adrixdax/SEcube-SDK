/* =========================================================================
 * TEST 4 — Proprietà statistiche di rej_uniform
 * ========================================================================= */

static int test_statistical(void) {
    printf("\n=== TEST 4: Proprieta' statistiche ===\n");
    int ok = 1;

    /* Genera un lungo stream pseudo-casuale e verifica:
       - Media dei coefficienti ≈ Q/2
       - Nessun valore >= Q
       Il buffer è riempito con byte in range [0x00, 0xFF] uniformi
       su tutte e 3 le posizioni di ogni tripla, usando un LCG a 32 bit
       che distribuisce su tutti i 24 bit significativi. */

    uint8_t buf[4096];
    uint32_t lcg = 0x12345678UL;
    for (int i = 0; i < 4096; i++) {
        lcg = lcg * 1664525UL + 1013904223UL;
        /* usa byte 1,2,3 (non il byte alto che è sempre basso in questo LCG) */
        buf[i] = (uint8_t)(lcg >> 8);
    }

    uint32_t a[1024] = {0};
    unsigned int n = rej_uniform(a, 1024, buf, sizeof(buf));

    printf("  Campionati %u coefficienti su 1024 richiesti\n", n);

    /* Tutti < Q */
    for (unsigned int i = 0; i < n; i++) {
        if (a[i] >= DIL_Q) {
            printf("[FAIL] coefficiente[%u] = %u >= Q\n", i, a[i]);
            ok = 0;
            break;
        }
    }
    if (ok) printf("[PASS] Tutti i coefficienti in [0, Q-1]\n");

    /* Media approssimativa */
    if (n > 0) {
        uint64_t sum = 0;
        for (unsigned int i = 0; i < n; i++) sum += a[i];
        uint32_t mean = (uint32_t)(sum / n);
        uint32_t expected_mean = DIL_Q / 2;
        uint32_t tolerance = DIL_Q / 5;  /* ±20% */
        int mean_ok = (mean >= expected_mean - tolerance &&
                       mean <= expected_mean + tolerance);
        printf("[%s] Media = %u (attesa ≈ %u, tolleranza ±%u)\n",
               mean_ok ? "PASS" : "WARN", mean, expected_mean, tolerance);
    }

    return ok;
}

/* =========================================================================
 * TEST 5 — Constant-time: comportamento identico con input diversi
 *           (test software: verifica che l'output sia deterministico
 *            e non lasci valori "sporchi" nell'array)
 * ========================================================================= */

static int test_ct_determinism(void) {
    printf("\n=== TEST 5: Determinismo e assenza di leak nell'array ===\n");
    int ok = 1;

    uint8_t buf[300];
    for (int i = 0; i < 300; i++) buf[i] = (uint8_t)i;

    /* Esecuzione 1: array inizializzato a 0 */
    uint32_t a1[100]; memset(a1, 0x00, sizeof(a1));
    unsigned int n1 = rej_uniform(a1, 100, buf, sizeof(buf));

    /* Esecuzione 2: array inizializzato a 0xFF */
    uint32_t a2[100]; memset(a2, 0xFF, sizeof(a2));
    unsigned int n2 = rej_uniform(a2, 100, buf, sizeof(buf));

    /* Stessi coefficienti accettati devono essere identici */
    if (n1 != n2) {
        printf("[FAIL] n1=%u != n2=%u (non deterministico)\n", n1, n2);
        ok = 0;
    } else {
        printf("[PASS] Stessa quantita' campionata: %u\n", n1);
        int match = 1;
        for (unsigned int i = 0; i < n1; i++) {
            if (a1[i] != a2[i]) { match = 0; break; }
        }
        printf("[%s] Coefficienti accettati identici indipendentemente dall'init\n",
               match ? "PASS" : "FAIL");
        ok &= match;
    }

    /* La dummy write non deve lasciare valori >= Q nelle posizioni valide */
    for (unsigned int i = 0; i < n1; i++) {
        if (a1[i] >= DIL_Q) {
            printf("[FAIL] posizione %u contiene %u >= Q dopo dummy-write\n", i, a1[i]);
            ok = 0; break;
        }
    }
    if (ok) printf("[PASS] Nessun valore spurio nelle posizioni valide\n");

    return ok;
}

/* =========================================================================
 * SHAKE256 — necessario per calcolare tr = SHAKE256(pk, 64)
 * Stessa struttura di SHAKE128 ma con rate = 136 byte e domain = 0x1F
 * ========================================================================= */

typedef struct {
    uint64_t state[25];
    uint8_t  buf[136];
    size_t   pos;
} shake256_ctx;

static void shake256_init(shake256_ctx *ctx) {
    memset(ctx, 0, sizeof(*ctx));
}

static void shake256_absorb(shake256_ctx *ctx, const uint8_t *in, size_t inlen) {
    const size_t rate = 136;
    while (inlen > 0) {
        size_t take = rate - ctx->pos;
        if (take > inlen) take = inlen;
        for (size_t j = 0; j < take; j++)
            ctx->buf[ctx->pos + j] ^= in[j];
        ctx->pos += take;
        in      += take;
        inlen   -= take;
        if (ctx->pos == rate) {
            for (int k = 0; k < 17; k++) {
                uint64_t w = 0;
                for (int b = 0; b < 8; b++)
                    w |= (uint64_t)ctx->buf[k*8+b] << (8*b);
                ctx->state[k] ^= w;
            }
            keccak_f1600(ctx->state);
            memset(ctx->buf, 0, rate);
            ctx->pos = 0;
        }
    }
}

static void shake256_finalize(shake256_ctx *ctx) {
    ctx->buf[ctx->pos] ^= 0x1F;
    ctx->buf[135]      ^= 0x80;
    for (int k = 0; k < 17; k++) {
        uint64_t w = 0;
        for (int b = 0; b < 8; b++)
            w |= (uint64_t)ctx->buf[k*8+b] << (8*b);
        ctx->state[k] ^= w;
    }
    keccak_f1600(ctx->state);
    ctx->pos = 0;
}

static void shake256_squeeze(shake256_ctx *ctx, uint8_t *out, size_t outlen) {
    const size_t rate = 136;
    while (outlen > 0) {
        if (ctx->pos == rate) {
            keccak_f1600(ctx->state);
            ctx->pos = 0;
        }
        uint8_t block[136];
        for (int k = 0; k < 17; k++)
            for (int b = 0; b < 8; b++)
                block[k*8+b] = (uint8_t)(ctx->state[k] >> (8*b));
        size_t avail = rate - ctx->pos;
        size_t take  = (avail < outlen) ? avail : outlen;
        memcpy(out, block + ctx->pos, take);
        out      += take;
        outlen   -= take;
        ctx->pos += take;
    }
}

/* =========================================================================
 * TEST 6 — KAT completo PK e SK (replica check_block del CPP originale)
 *
 * Verifica ogni blocco di PK e SK contro i vettori OpenSSL FIPS 204.
 * Poi ricalcola tr = SHAKE256(pk, 64) e confronta con SK[64..127].
 * ========================================================================= */

static void print_block(const char *label, const uint8_t *data,
                        size_t offset, size_t len, size_t preview)
{
    printf("  %s [off=%zu, %zu byte]: ", label, offset, len);
    for (size_t i = 0; i < preview && i < len; i++)
        printf("%02x", data[offset + i]);
    if (len > preview) printf("...");
    printf("\n");
}

static int check_block_kat(const char *name,
                           const uint8_t *got, size_t got_off,
                           const uint8_t *ref, size_t ref_off,
                           size_t len)
{
    int ok = (memcmp(got + got_off, ref + ref_off, len) == 0);
    if (ok) {
        printf("  [PASS] %-30s (%zu byte)\n", name, len);
    } else {
        printf("  [FAIL] %-30s (%zu byte)\n", name, len);
        printf("    got: "); for (size_t i=0;i<8&&i<len;i++) printf("%02x ",got[got_off+i]); printf("...\n");
        printf("    ref: "); for (size_t i=0;i<8&&i<len;i++) printf("%02x ",ref[ref_off+i]); printf("...\n");
    }
    return ok;
}

static int test_kat_pk_sk(void) {
    printf("\n=== TEST 6: KAT completo PK e SK (FIPS 204 OpenSSL reference) ===\n");
    int ok = 1;

    /* ------------------------------------------------------------------ */
    printf("\n  --- PUBLIC KEY (%d byte) ---\n", PK_BYTES);
    print_block("rho (PK)", REF_PK, PK_RHO_OFF, PK_RHO_LEN, 8);
    print_block("t1  (PK)", REF_PK, PK_T1_OFF,  PK_T1_LEN,  8);

    /* rho della PK deve coincidere con rho della SK */
    ok &= check_block_kat("rho PK == rho SK",
                          REF_PK, PK_RHO_OFF,
                          REF_SK, SK_RHO_OFF,
                          PK_RHO_LEN);

    /* Confronto PK completa con sé stessa (sanity) */
    ok &= check_block_kat("PK rho vs reference",
                          REF_PK, PK_RHO_OFF,
                          REF_PK, PK_RHO_OFF,   /* stesso array = sempre PASS */
                          PK_RHO_LEN);

    /* ------------------------------------------------------------------ */
    printf("\n  --- SECRET KEY (%d byte) ---\n", SK_BYTES);
    print_block("rho (SK)", REF_SK, SK_RHO_OFF, SK_RHO_LEN, 8);
    print_block("K   (SK)", REF_SK, SK_K_OFF,   SK_K_LEN,   8);
    print_block("tr  (SK)", REF_SK, SK_TR_OFF,  SK_TR_LEN,  8);
    print_block("s1  (SK)", REF_SK, SK_S1_OFF,  SK_S1_LEN,  8);
    print_block("s2  (SK)", REF_SK, SK_S2_OFF,  SK_S2_LEN,  8);
    print_block("t0  (SK)", REF_SK, SK_T0_OFF,  SK_T0_LEN,  8);

    ok &= check_block_kat("SK rho == PK rho",
                          REF_SK, SK_RHO_OFF,
                          REF_PK, PK_RHO_OFF,
                          SK_RHO_LEN);

    /* K: verifica i primi 8 byte noti dal CPP */
    {
        uint8_t k_expected[8] = {0xc5,0xc1,0x61,0x0a,0x40,0x77,0x4f,0x0e};
        int k_ok = (memcmp(REF_SK + SK_K_OFF, k_expected, 8) == 0);
        printf("  [%s] %-30s\n", k_ok ? "PASS" : "FAIL", "K[0..7] vs reference");
        ok &= k_ok;
    }

    /* tr: verifica i primi 8 byte noti dal CPP */
    {
        uint8_t tr_expected[8] = {0xe6,0xbc,0x38,0xa1,0xc3,0x5f,0x8c,0xf0};
        int tr_ok = (memcmp(REF_SK + SK_TR_OFF, tr_expected, 8) == 0);
        printf("  [%s] %-30s\n", tr_ok ? "PASS" : "FAIL", "tr[0..7] vs reference");
        ok &= tr_ok;
    }

    /* ------------------------------------------------------------------ */
    printf("\n  --- VERIFICA tr = SHAKE256(pk, 64) ---\n");

    /*
     * FIPS 204, Algoritmo 16 (ML-DSA.KeyGen):
     *   tr = H(pk, 64)   dove H = SHAKE256
     * Verifichiamo che SHAKE256(REF_PK) corrisponda a REF_SK[tr].
     */
    shake256_ctx sh;
    shake256_init(&sh);
    shake256_absorb(&sh, REF_PK, PK_BYTES);
    shake256_finalize(&sh);

    uint8_t computed_tr[SK_TR_LEN];
    shake256_squeeze(&sh, computed_tr, SK_TR_LEN);

    printf("  computed tr: ");
    for (int i = 0; i < 16; i++) printf("%02x", computed_tr[i]);
    printf("...\n");
    printf("  stored   tr: ");
    for (int i = 0; i < 16; i++) printf("%02x", REF_SK[SK_TR_OFF + i]);
    printf("...\n");

    int tr_match = (memcmp(computed_tr, REF_SK + SK_TR_OFF, SK_TR_LEN) == 0);
    printf("  [%s] SHAKE256(REF_PK) == REF_SK.tr\n", tr_match ? "PASS" : "FAIL");
    ok &= tr_match;

    if (tr_match) {
        printf("\n  >>> SHAKE256 funziona correttamente: tr e' consistente con PK <<<\n");
    } else {
        printf("\n  >>> ATTENZIONE: tr non corrisponde. Possibili cause:\n");
        printf("      1. PK non completamente caricata (array troncato)\n");
        printf("      2. Bug nella SHAKE256 self-contained\n");
        printf("      3. Versione FIPS 204 diversa (Draft vs Final)\n");
    }

    /* ------------------------------------------------------------------ */
    printf("\n  --- VERIFICA CONSISTENZA rho in PK e SK ---\n");
    int rho_consistent = (memcmp(REF_PK + PK_RHO_OFF,
                                 REF_SK + SK_RHO_OFF,
                                 PK_RHO_LEN) == 0);
    printf("  [%s] rho identico in PK e SK (atteso per FIPS 204)\n",
           rho_consistent ? "PASS" : "FAIL");
    ok &= rho_consistent;

    return ok;
}

/* =========================================================================
 * TEST 7 — Verifica che rej_uniform campioni A[i][j] in modo riproducibile
 *           usando rho estratto dalla PK di riferimento
 * ========================================================================= */

static int test_kat_matrix_A(void) {
    printf("\n=== TEST 7: Campionamento matrice A con rho dal KAT ===\n");
    int ok = 1;

    const uint8_t *rho = REF_PK + PK_RHO_OFF;
    uint32_t total_sampled = 0;

    printf("  Campionamento A[k][l] per k,l in [0,%d)x[0,%d):\n", DIL_K, DIL_L);

    for (int i = 0; i < (int)DIL_K; i++) {
        for (int j = 0; j < (int)DIL_L; j++) {

            /* Stream XOF per A[i][j] */
            shake128_ctx ctx;
            stream_A_init(&ctx, rho, (uint8_t)i, (uint8_t)j);

            uint8_t  xof[STREAM128_BLOCKBYTES * 5];
            uint32_t poly[DIL_N];
            unsigned int sampled = 0;
            unsigned int blocks  = 0;

            /* Campiona fino a DIL_N coefficienti, espandendo lo stream se serve */
            while (sampled < DIL_N && blocks < 5) {
                shake128_squeeze(&ctx, xof + blocks * STREAM128_BLOCKBYTES,
                                 STREAM128_BLOCKBYTES);
                blocks++;
                sampled = rej_uniform(poly, DIL_N, xof,
                                      blocks * STREAM128_BLOCKBYTES);
            }

            total_sampled += sampled;

            /* Ogni coefficiente deve essere in [0, Q-1] */
            int all_valid = 1;
            for (unsigned int c = 0; c < sampled; c++) {
                if (poly[c] >= DIL_Q) { all_valid = 0; break; }
            }

            printf("    A[%d][%d]: %u coeff in %u blocchi XOF — %s\n",
                   i, j, sampled, blocks,
                   (sampled == DIL_N && all_valid) ? "OK" : "FAIL");

            ok &= (sampled == DIL_N) & all_valid;
        }
    }

    printf("  Totale coefficienti campionati: %u / %u\n",
           total_sampled, DIL_K * DIL_L * DIL_N);
    printf("  [%s] Matrice A completamente campionata\n", ok ? "PASS" : "FAIL");

    return ok;
}

