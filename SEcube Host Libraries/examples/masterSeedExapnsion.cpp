/* =========================================================================
 * Implementazione minimale SHAKE128 (Keccak) per derivare lo stream XOF
 * In produzione, usa fips202.c dal reference Dilithium/NIST.
 * Qui includiamo una versione self-contained per portabilità.
 * ========================================================================= */

/* --- Keccak-f[1600] permutation --- */
static const uint64_t KeccakRoundConstants[24] = {
    0x0000000000000001ULL, 0x0000000000008082ULL, 0x800000000000808aULL,
    0x8000000080008000ULL, 0x000000000000808bULL, 0x0000000080000001ULL,
    0x8000000080008081ULL, 0x8000000000008009ULL, 0x000000000000008aULL,
    0x0000000000000088ULL, 0x0000000080008009ULL, 0x000000008000000aULL,
    0x000000008000808bULL, 0x800000000000008bULL, 0x8000000000008089ULL,
    0x8000000000008003ULL, 0x8000000000008002ULL, 0x8000000000000080ULL,
    0x000000000000800aULL, 0x800000008000000aULL, 0x8000000080008081ULL,
    0x8000000000008080ULL, 0x0000000080000001ULL, 0x8000000080008008ULL
};

static const int KeccakRhoOffsets[25] = {
     0,  1, 62, 28, 27, 36, 44,  6, 55, 20,
     3, 10, 43, 25, 39, 41, 45, 15, 21,  8,
    18,  2, 61, 56, 14
};

static inline uint64_t rotl64(uint64_t x, int n) {
    return (x << n) | (x >> (64 - n));
}

static void keccak_f1600(uint64_t state[25]) {
    uint64_t C[5], D[5], B[25];
    for (int r = 0; r < 24; r++) {
        /* Theta */
        for (int x = 0; x < 5; x++)
            C[x] = state[x] ^ state[x+5] ^ state[x+10] ^ state[x+15] ^ state[x+20];
        for (int x = 0; x < 5; x++)
            D[x] = C[(x+4)%5] ^ rotl64(C[(x+1)%5], 1);
        for (int i = 0; i < 25; i++)
            state[i] ^= D[i % 5];
        /* Rho + Pi */
        for (int i = 0; i < 25; i++) {
            int x = i % 5, y = i / 5;
            B[y + 5 * ((2*x + 3*y) % 5)] = rotl64(state[i], KeccakRhoOffsets[i]);
            (void)x; (void)y;
        }
        /* Chi */
        for (int i = 0; i < 25; i++) {
            int x = i % 5, y = (i / 5) * 5;
            state[i] = B[y + x] ^ (~B[y + (x+1)%5] & B[y + (x+2)%5]);
        }
        /* Iota */
        state[0] ^= KeccakRoundConstants[r];
    }
}

typedef struct {
    uint64_t state[25];
    uint8_t  buf[168];   /* rate SHAKE128 */
    size_t   pos;
} shake128_ctx;

static void shake128_init(shake128_ctx *ctx) {
    memset(ctx, 0, sizeof(*ctx));
}

static void shake128_absorb(shake128_ctx *ctx, const uint8_t *in, size_t inlen) {
    size_t rate = 168;
    size_t i = 0;
    while (i < inlen) {
        size_t take = rate - ctx->pos;
        if (take > inlen - i) take = inlen - i;
        for (size_t j = 0; j < take; j++)
            ctx->buf[ctx->pos + j] ^= in[i + j];
        ctx->pos += take;
        i += take;
        if (ctx->pos == rate) {
            /* absorb block */
            for (int k = 0; k < 21; k++) {
                uint64_t w = 0;
                for (int b = 0; b < 8; b++)
                    w |= (uint64_t)ctx->buf[k*8+b] << (8*b);
                ctx->state[k] ^= w;
            }
            keccak_f1600(ctx->state);
            memset(ctx->buf, 0, rate);
            ctx->pos = 0;
        }
    }
}

static void shake128_finalize(shake128_ctx *ctx) {
    ctx->buf[ctx->pos] ^= 0x1F;
    ctx->buf[167]      ^= 0x80;
    for (int k = 0; k < 21; k++) {
        uint64_t w = 0;
        for (int b = 0; b < 8; b++)
            w |= (uint64_t)ctx->buf[k*8+b] << (8*b);
        ctx->state[k] ^= w;
    }
    keccak_f1600(ctx->state);
    ctx->pos = 0;
}

static void shake128_squeeze(shake128_ctx *ctx, uint8_t *out, size_t outlen) {
    /*
     * Bug precedente: serializzava lo state solo all'inizio del ciclo
     * (condizione ctx->pos==0 || ctx->pos==rate), poi copiava con offset
     * ctx->pos % rate sempre uguale a 0 → leggeva sempre il primo blocco.
     *
     * Fix: mantieni un buffer serializzato esplicito per ogni blocco,
     * avanza il cursore byte per byte, chiama keccak quando il buffer
     * è esaurito.
     */
    const size_t rate = 168;

    while (outlen > 0) {
        /* Se il cursore ha consumato l'intero blocco corrente, permuta */
        if (ctx->pos == rate) {
            keccak_f1600(ctx->state);
            ctx->pos = 0;
        }

        /* Serializza lo state corrente in un blocco temporaneo */
        uint8_t block[168];
        for (int k = 0; k < 21; k++)
            for (int b = 0; b < 8; b++)
                block[k*8+b] = (uint8_t)(ctx->state[k] >> (8*b));

        /* Copia dalla posizione corrente fino a fine blocco o fine output */
        size_t avail = rate - ctx->pos;
        size_t take  = (avail < outlen) ? avail : outlen;
        memcpy(out, block + ctx->pos, take);

        out      += take;
        outlen   -= take;
        ctx->pos += take;
    }
}

/* =========================================================================
 * Derivazione dello stream XOF per rej_uniform
 * FIPS 204: stream_A(rho, i, j) = SHAKE128(rho || j || i)
 * ========================================================================= */

static void stream_A_init(shake128_ctx *ctx,
                          const uint8_t rho[32],
                          uint8_t i, uint8_t j)
{
    shake128_init(ctx);
    shake128_absorb(ctx, rho, 32);
    uint8_t nonce[2] = { j, i };  /* FIPS 204: little-endian (j, i) */
    shake128_absorb(ctx, nonce, 2);
    shake128_finalize(ctx);
}

